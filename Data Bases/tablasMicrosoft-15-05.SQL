CREATE TABLE PROVEEDORES (
NIT INT,
COD INT,
NOMB VARCHAR (32),
NOMGEREN VARCHAR (32),
CEDGEREN VARCHAR (32),
TELGEREN VARCHAR (32),
CIUDAD VARCHAR (32),
DIRECC VARCHAR (32),
TEL VARCHAR (32),
EMAIL VARCHAR (32),
PRIMARY KEY (NIT)
)



CREATE TABLE SUCURSALES (
CIUDAD VARCHAR (32),
NOMDIREC VARCHAR (32),
TEL INT,
PRIMARY KEY (CIUDAD)
)


CREATE TABLE CARGOS (
CARGO VARCHAR (32),
SALARIOMIN INT,
SALARIOMAX INT,
PRIMARY KEY (CARGO)
)


CREATE TABLE PRODUCTOS (
IDPRO INT,
DESCRIP VARCHAR (32) UNIQUE,
UNIDAD INT,
PRECIO INT,
NITPROVEEDOR INT,
PRIMARY KEY (IDPRO),
FOREIGN KEY (NITPROVEEDOR) REFERENCES PROVEEDORES
)


CREATE TABLE EMPLEADOS (
CED INT,
NOMB VARCHAR (32),
APELL VARCHAR (32),
TEL INT,
DIRECC VARCHAR (32),
GENERO VARCHAR (9),
EMAIL VARCHAR (32),
FECHAINGRE DATE,
DEPARTAMENTO VARCHAR (32),
CARGO VARCHAR (32),
SUCURSAL VARCHAR (32),
JEFE VARCHAR (32) NULL,
SALARIO INT,
COMISION INT NULL,
PRIMARY KEY (CED),
FOREIGN KEY (CARGO) REFERENCES CARGOS,
FOREIGN KEY (SUCURSAL) REFERENCES SUCURSALES,
)


CREATE TABLE CLIENTES (
NIT INT,
NOMB VARCHAR (32),
APELL VARCHAR (32),
SUCURSAL VARCHAR (32),
DIRECC VARCHAR (32),
EMAIL VARCHAR (32),
TEL INT,
IDPRO INT,
DESCRIP VARCHAR (32),
CEDVENDEDOR INT,
PRIMARY KEY (NIT),
FOREIGN KEY (SUCURSAL) REFERENCES SUCURSALES,
FOREIGN KEY (IDPRO) REFERENCES PRODUCTOS,
FOREIGN KEY (DESCRIP) REFERENCES PRODUCTOS(DESCRIP),
FOREIGN KEY (CEDVENDEDOR) REFERENCES EMPLEADOS
)

